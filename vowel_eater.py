# Prompt the user to enter a word
user_word = input("Enter a word: ").upper() #converting the user's input to UPPER CASES as instructed

for letter in user_word: 
    # Complete the body of the for loop.
    if letter == 'A':
        continue
    elif letter == 'E':
        continue
    elif letter == 'I':
        continue
    elif letter == 'O':
        continue
    elif letter == 'U':
        continue
    else: 
        print(letter)
